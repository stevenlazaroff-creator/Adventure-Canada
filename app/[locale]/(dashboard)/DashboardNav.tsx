export function DashboardNav({ subscription }: DashboardNavProps) {
  const pathname = usePathname()
  const currentTier = subscription?.tier || 'free'

  // Extract locale from pathname
  const locale = pathname.split('/')[1] || 'en'
  // Remove locale prefix for comparison
  const pathnameWithoutLocale = pathname.replace(/^\/(en|fr)/, '')

  return (
    <nav className="space-y-1">
      {navItems.map((item) => {
        const isActive = pathnameWithoutLocale === item.href ||
          (item.href !== '/dashboard' && pathnameWithoutLocale.startsWith(item.href))

        const isLocked = item.requiresTier && !item.requiresTier.includes(currentTier)

        return (
          <Link
            key={item.href}
            href={isLocked ? `/${locale}/dashboard/billing` : `/${locale}${item.href}`}
            className={cn(
              'flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium transition-colors',
              isActive
                ? 'bg-primary-50 text-primary-700'
                : 'text-gray-600 hover:bg-gray-100 hover:text-gray-900',
              isLocked && 'opacity-50'
            )}
          >
            {item.icon}
            <span>{item.label}</span>
            {isLocked && (
              <span className="ml-auto text-xs bg-gray-200 text-gray-600 px-2 py-0.5 rounded">
                Pro
              </span>
            )}
          </Link>
        )
      })}
    </nav>
  )
}
